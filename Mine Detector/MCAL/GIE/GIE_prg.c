#include "../../LIB/STD_TYPES.h"
#include "../../LIB/BIT_MATH.h"
#include "GIE_reg.h"
#include"GIE_int.h"

/*************************************************************************************************/
/********************************Function: Enable Global Interrupt********************************/
/********************************Inputs:void******************************************************/
/*************************************************************************************************/
void GIE_vEnableGlobalInterrupt(void)
{
	SET_BIT(SREG,I);
}

/*************************************************************************************************/
/********************************Function: Disable Global Interrupt*******************************/
/********************************Inputs:void******************************************************/
/*************************************************************************************************/
void GIE_vDisableGlobalInterrupt(void)
{
	CLR_BIT(SREG,I);
}
